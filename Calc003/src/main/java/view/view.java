    private void jButtonDividirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDividirActionPerformed
        // TODO add your handling code here:
        if(JTFNum1.getText().trim().isEmpty() || JTFNum2.getText().trim().isEmpty()){
            JTFResultado.setText("Preencha todos os campos");
            JTAHistorico.setText("preencha todos os campos");
            JTFNum1.requestFocus();
            return;
        }
        try{
            double num1 = Double.parseDouble(JTFNum1.getText());
            double num2 = Double.parseDouble(JTFNum2.getText());
            
            Calculadora calculadora = new Calculadora();
            double resultado = calculadora.dividir(num1,num2);
            
            JTFResultado.setText("resultado: "+ resultado);
            
            JTAHistorico.append(num1 + " / " + num2 +" = " + resultado + "\n");
            String linha = num1 + " / " + num2 + " = " + resultado;
            Arquivo.salvarLinha(linha);
        }
        catch(NumberFormatException ex){
            JTFResultado.setText("digite apenas valor numerico");
        }
        catch(ArithmeticException ex){
            JTFResultado.setText(ex.getMessage());
        }
          
    }//GEN-LAST:event_jButtonDividirActionPerformed

