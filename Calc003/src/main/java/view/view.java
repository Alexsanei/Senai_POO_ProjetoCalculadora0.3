    private void jButtonExcluirHistoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirHistoricoActionPerformed
        boolean sucesso = Arquivo.excluirArquivo("Historico.txt");
        if (sucesso) {
            javax.swing.JOptionPane.showMessageDialog(this, "Arquivo 'Historico.txt' excluído com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            JTAHistorico.setText(""); // Limpa o JTAHistorico / o arquivo foi excluído
        } else {
            // Se o método retornar false, significa que o arquivo não existia, ou houve erro de permissão, etc.
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Falha ao excluir o arquivo 'Historico.txt'. Verifique se ele existe e não está em uso.", 
                "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }       
        JTFNum1.requestFocus(); 
        JTAHistorico.setText("");
        JTFNum1.requestFocus();
    }//GEN-LAST:event_jButtonExcluirHistoricoActionPerformed

